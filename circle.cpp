
#include "circle.h"
#include <cmath>

void myShapes::Circle::calcCoordinates()
{
    int n = 8;
    for (int i = 0; i < n; ++i) {
        double fAngle = 2.0 * 3.14 * i / n;
        int x = (int)(50 + std::cos(fAngle) * 40);
        int y = (int)(50 + std::sin(fAngle) * 40);
        m_points.push_back(std::make_pair(x, y));
    }
}
